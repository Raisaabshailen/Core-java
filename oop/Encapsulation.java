package oop;

public class Encapsulation {
	
    private String a;
    private int b;
    
    
    
    public String geta() {
    	return a;
    }
    public void seta(String a) {
    	this.a=a;
    }
    public int getb() {
    	return b;
    }
    public void setb(int b) {
    	this.b=b;
    }

}
